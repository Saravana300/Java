package corejava;

public class Bitwise {
	public static void main(String[] args) {
		int a=10;
		int b=5;
		System.out.println(a&b);
		System.out.println(a|b);
		System.out.println(~a);
		System.out.println(a^b);
		// ^ - Xor operator,in logical table any one operand 1 or 0 is 1,if both operand are 1 or 0 is 0
	}
}
